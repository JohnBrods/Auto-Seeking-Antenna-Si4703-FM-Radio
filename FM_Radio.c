void Stop_Tune(unsigned char ChannelSet){

        Soft_I2C_Start();
        Soft_I2C_Write(0x20);

        Soft_I2C_Write(0x42);     //
        Soft_I2C_Write(0x01);     //REG 2 SEEK ENABLE BIT = ZERO AND DISABLE MUTE = 1

        Soft_I2C_Write(0x00);
        Soft_I2C_Write(ChannelSet);     //REG 3  channel     0.1 * 145 + 875 = 102
        Soft_I2C_Stop();
}

void Seek(unsigned char DataHigh){

        Soft_I2C_Start();
        Soft_I2C_Write(0x20);

        Soft_I2C_Write(0x42 | DataHigh);
        Soft_I2C_Write(0x01);     //REG 2
        Soft_I2C_Stop();
}

void Tune(unsigned char ChannelSet){

        Soft_I2C_Start();
        Soft_I2C_Write(0x20);

        Soft_I2C_Write(0x42);     //40 IS DISABLE MUTE
        Soft_I2C_Write(0x01);     //REG 2

        Soft_I2C_Write(0x80);     //80 = TUNE BIT
        Soft_I2C_Write(ChannelSet);       //REG 3  channel     0.1 * 145 + 87.5 = 102 -- 145 = 102 = Greatest Hits FM 102 Mhz FM  //153 Hallam FM   //176 = Capital FM  105.1 Mhz

        Soft_I2C_Write(0b00011000);  //bit 12 is RDS ENABLE ON
        Soft_I2C_Write(0);          //REG 4  SYSCONFIG 1 RDS DE ETC

        Soft_I2C_Write(33);   //RSSI
        Soft_I2C_Write(0b00010011);     //REG 5 volume and 100khz spacing = bit 5 and bit 4 = 01

        Soft_I2C_Stop();
}

